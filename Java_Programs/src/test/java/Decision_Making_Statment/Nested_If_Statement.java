package Decision_Making_Statment;

public class Nested_If_Statement {

	public static void main(String[] args) {
		int n=10;
		if (n>5) {
			if (n%2==0) {
				System.out.println("It is a Even No");
			}else {
				System.out.println("It is a odd No");
			}
		}else {
			System.out.println("It's small than 5 no");
		}

	}

}
