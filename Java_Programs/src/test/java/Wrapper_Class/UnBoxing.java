package Wrapper_Class;

public class UnBoxing {

	public static void main(String[] args) {
		Integer obj=50;
		int n=obj.intValue();
		System.out.println(obj);
		System.out.println(n);
		
		Boolean obj1=true;
		boolean n1=obj1.booleanValue();
		System.out.println(obj1);
		System.out.println(n1);

	}

}
